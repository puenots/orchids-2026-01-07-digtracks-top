// i18n Module
const i18n = {
  currentLocale: 'ja',

  // Initialize i18n
  init() {
    // Check localStorage for saved locale
    const savedLocale = localStorage.getItem('digtracks-locale');
    if (savedLocale && translations[savedLocale]) {
      this.currentLocale = savedLocale;
    } else {
      // Detect browser language
      const browserLang = navigator.language.split('-')[0];
      if (translations[browserLang]) {
        this.currentLocale = browserLang;
      }
    }

    this.updateUI();
    this.updateLangDisplay();
  },

  // Set locale
  setLocale(locale) {
    if (translations[locale]) {
      this.currentLocale = locale;
      localStorage.setItem('digtracks-locale', locale);
      this.updateUI();
      this.updateLangDisplay();

      // Dispatch custom event for other scripts
      window.dispatchEvent(new CustomEvent('localeChanged', { detail: { locale } }));
    }
  },

  // Get translation by key path (e.g., "Header.nav.music")
  t(keyPath) {
    const keys = keyPath.split('.');
    let value = translations[this.currentLocale];

    for (const key of keys) {
      if (value && typeof value === 'object' && key in value) {
        value = value[key];
      } else {
        // Fallback to English
        value = translations.en;
        for (const k of keys) {
          if (value && typeof value === 'object' && k in value) {
            value = value[k];
          } else {
            return keyPath; // Return key if not found
          }
        }
        break;
      }
    }

    return value;
  },

  // Update all UI elements with translations
  updateUI() {
    // Update text content
    document.querySelectorAll('[data-i18n]').forEach(el => {
      const key = el.getAttribute('data-i18n');
      const translated = this.t(key);
      // Only update if translation was found (not returning the key itself)
      if (translated !== key) {
        el.textContent = translated;
      }
    });

    // Update HTML content (for elements with <br> etc.)
    document.querySelectorAll('[data-i18n-html]').forEach(el => {
      const key = el.getAttribute('data-i18n-html');
      const translated = this.t(key);
      // Only update if translation was found (not returning the key itself)
      if (translated !== key) {
        el.innerHTML = translated;
      }
    });

    // Update placeholders
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
      const key = el.getAttribute('data-i18n-placeholder');
      const translated = this.t(key);
      // Only update if translation was found (not returning the key itself)
      if (translated !== key) {
        el.placeholder = translated;
      }
    });

    // Update HTML lang attribute
    document.documentElement.lang = this.currentLocale;

    // Update name field order for Japanese
    this.updateNameFieldOrder();
  },

  // Update language display in UI
  updateLangDisplay() {
    const langDisplay = this.currentLocale.toUpperCase();
    const currentLangEl = document.getElementById('current-lang');
    const mobileCurrentLangEl = document.getElementById('mobile-current-lang');

    if (currentLangEl) currentLangEl.textContent = langDisplay;
    if (mobileCurrentLangEl) mobileCurrentLangEl.textContent = langDisplay;
  },

  // Update name field order (Japanese: lastName first, English: firstName first)
  updateNameFieldOrder() {
    const nameFields = document.getElementById('name-fields');
    if (!nameFields) return;

    const firstNameGroup = nameFields.querySelector('#firstName')?.closest('.form-group');
    const lastNameGroup = nameFields.querySelector('#lastName')?.closest('.form-group');

    if (!firstNameGroup || !lastNameGroup) return;

    if (this.currentLocale === 'ja') {
      // Japanese: lastName first
      nameFields.insertBefore(lastNameGroup, firstNameGroup);
    } else {
      // English: firstName first
      nameFields.insertBefore(firstNameGroup, lastNameGroup);
    }
  },

  // Check if current locale is Japanese
  isJapanese() {
    return this.currentLocale === 'ja';
  }
};

// Export for use in other scripts
window.i18n = i18n;
window.translations = translations;
